# mengkonfersi celcius ke fahrenheit

# suhu dalam Celsius
celsius = 10

# konversi ke Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# hasil

print("hasil dari konfersi 10 celsius ke fahrenheit adalah", fahrenheit )


# mengkonfersi fahrenheit ke celcius

# Suhu dalam Fahrenheit
fahrenheit = 50

# Konversi ke Celsius
celsius = (5/9) * (fahrenheit - 32)

# hasil
print(f"{fahrenheit} Fahrenheit sama dengan {celsius} Celsius")


